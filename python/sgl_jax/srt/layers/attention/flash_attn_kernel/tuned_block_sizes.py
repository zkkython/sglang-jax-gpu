def get_tuned_block_sizes(
    q_dtype,
    kv_dtype,
    actual_num_q_heads,
    actual_num_kv_heads,
    head_dim,
    page_size,
    max_num_tokens,
    pages_per_seq,
) -> tuple[int, int]:
    """Look up for the best (num_kv_pages_per_blk, num_queries_per_blk) from auto-tuned table."""
    tpu_version = get_tpu_version()

    if tpu_version < 4:
        raise NotImplementedError("TPU version must be 4 or higher.")
    keys = get_simplified_key(
        page_size,
        q_dtype,
        kv_dtype,
        actual_num_q_heads,
        actual_num_kv_heads,
        head_dim,
        page_size * pages_per_seq,
    )
    device, page_size, dtypes, head_dims, max_model_len = keys

    # Default block sizes.
    bkv_p, bq = (1024 // page_size, 32)
    if tpu_version == 4:
        # TPUv4 has much smaller VMEM size so we pick fixed block sizes.
        bkv_p, bq = (512 // page_size, 32)
    else:
        if device in TUNED_BLOCK_SIZES:
            tb = TUNED_BLOCK_SIZES[device]
            if page_size in tb:
                tb = tb[page_size]
                if dtypes in tb:
                    tb = tb[dtypes]
                    if head_dims in tb:
                        tb = tb[head_dims]
                        if max_model_len in tb:
                            bkv_p, bq = tb[max_model_len]
    return (min(pages_per_seq, bkv_p), min(max_num_tokens, bq))


def get_simplified_key(
    page_size,
    q_dtype,
    kv_dtype,
    num_q_heads,
    num_kv_heads,
    head_dim,
    max_model_len,
):
    """Get the simplified key to reduce the number of combinations."""
    assert num_q_heads % num_kv_heads == 0
    num_q_heads_per_kv_head = num_q_heads // num_kv_heads
    q_packing = get_dtype_packing(q_dtype)
    kv_packing = get_dtype_packing(kv_dtype)
    num_kv_heads_x2 = align_to(num_kv_heads * 2, kv_packing)
    num_q_heads_per_kv_head = align_to(num_q_heads_per_kv_head, q_packing)
    assert num_kv_heads_x2 % 2 == 0
    num_q_heads = num_q_heads_per_kv_head * num_kv_heads
    num_kv_heads = num_kv_heads_x2 // 2

    device = get_device_name()
    q_dtype_name = jnp.dtype(q_dtype).name
    kv_dtype_name = jnp.dtype(kv_dtype).name
    num_q_heads = next_power_of_2(num_q_heads)
    num_kv_heads = next_power_of_2(num_kv_heads)

    return (
        device,
        next_power_of_2(page_size),
        f"q_{q_dtype_name}_kv_{kv_dtype_name}",
        f"q_head-{num_q_heads}_kv_head-{num_kv_heads}_head-{head_dim}",
        next_power_of_2(max_model_len),
    )
